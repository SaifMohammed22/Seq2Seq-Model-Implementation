import matplotlib.pyplot as plt
import time
import math

def asMinutes(s):
    m = math.floor(s / 60)
    s -= m * 60
    return '%dm %ds' % (m, s)

def timeSince(since, percent):
    now = time.time()
    s = now - since
    es = s / (percent)
    rs = es - s
    return '%s (- %s)' % (asMinutes(s), asMinutes(rs))

def showPlot(points):
    plt.figure()
    fig, ax = plt.subplots()
    ax.plot(points)

    ax.set_xlabel('Iterations (x100)')
    ax.set_ylabel('Loss')
    ax.set_title('Training Loss Over Time')

    plt.show()